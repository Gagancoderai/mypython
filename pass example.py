# Pass Statement Example
print("Demonstrating the 'pass' statement:")
for i in range(1, 11):
    # This 'pass' statement does nothing, it's just a placeholder
    pass
    print(f"Current number: {i}")

print("\nIn this example, 'pass' allowed the loop to continue without any specific action when iterating.")
print("It's useful when syntax requires a statement but no action is desired (e.g., in an empty function or conditional block).")
